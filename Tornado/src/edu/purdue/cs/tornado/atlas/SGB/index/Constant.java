package edu.purdue.cs.tornado.atlas.SGB.index;

public class Constant {

	  //the dimension of nodes
	 static int DIMS_NUMB=2;
	  //the side number of rectangle bound. 
	 static int SIDES_NUMB=2*DIMS_NUMB;
	  
	public int getDIMS_NUMB() {
		return DIMS_NUMB;
	}
	
	public void setDIMS_NUMB(int dIMS_NUMB) {
		DIMS_NUMB = dIMS_NUMB;
	}
	
	public int getSIDES_NUMB() {
		return SIDES_NUMB;
	}
	
	public void setSIDES_NUMB(int sIDES_NUMB) {
		SIDES_NUMB = sIDES_NUMB;
	}
	  
	  

}
